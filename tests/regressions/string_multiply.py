# Regression: string repetition / multiplication

# Basic repetition
print("ab" * 3)         # ababab

# Zero repetition
print("x" * 0)          # (empty string)

# int on left side
print(3 * "ha")         # hahaha

# Repetition via variable
n = 4
s = "-"
print(s * n)            # ----

# Multichar string
print("abc" * 2)        # abcabc

# One repetition
print("hello" * 1)      # hello
